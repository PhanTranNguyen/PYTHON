# a. Xây dựng cấu trúc SINHVIEN
class SinhVien:
    def __init__(self, ma, ten, namsinh, dtb):
        self.ma = ma
        self.ten = ten
        self.namsinh = namsinh
        self.dtb = dtb


# b. Nhập danh sách sinh viên
def nhap_ds(n):
    ds = []
    for i in range(n):
        print(f"\nNhập sinh viên {i+1}:")
        ma = input("Mã SV (10 ký tự): ")
        ten = input("Tên (<=20 ký tự): ")
        namsinh = int(input("Năm sinh: "))
        dtb = float(input("Điểm TB: "))
        sv = SinhVien(ma, ten, namsinh, dtb)
        ds.append(sv)
    return ds


# c. Đếm sinh viên đủ điều kiện lên lớp (dtb >= 5)
def dem_len_lop(ds):
    dem = 0
    for sv in ds:
        if sv.dtb >= 5:
            dem += 1
    return dem


# d. Xuất sinh viên đủ 20 tuổi
def sv_20_tuoi(ds):
    nam_hien_tai = 2026  # bạn có thể sửa lại
    print("\nSinh viên đủ 20 tuổi:")
    for sv in ds:
        if nam_hien_tai - sv.namsinh == 20:
            print(sv.ten, "-", sv.ma)


# e. Đếm sinh viên hệ đại học (có "DH" ở vị trí 2,3)
def dem_he_dh(ds):
    dem = 0
    for sv in ds:
        if len(sv.ma) >= 4 and sv.ma[2:4] == "DH":
            dem += 1
    return dem


# f. Đếm sinh viên tên "Lan"
def dem_ten_lan(ds):
    dem = 0
    for sv in ds:
        if sv.ten.strip().split()[-1] == "Lan":
            dem += 1
    return dem


# g. Đếm sinh viên họ "Phan"
def dem_ho_phan(ds):
    dem = 0
    for sv in ds:
        if sv.ten.strip().split()[0] == "Phan":
            dem += 1
    return dem


