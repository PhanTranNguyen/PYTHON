def tim_dong(a):
    if not a:
        print("Mảng rỗng, không có dòng để kiểm tra.")
        return
    found = False
    print("Tìm dòng có tổng >= 45:")
    for i in range(len(a)):
        if sum(a[i]) >= 45:
            print(f"Dòng {i} có tổng >= 45: {a[i]}")
            found = True
    if not found:
        print("Không tìm thấy dòng nào có tổng >= 45.")

# e. Tìm cột có tích nhỏ nhất
def cot_tich_nho_nhat(a):
    if not a or not a[0]:
        print("Mảng rỗng hoặc không có cột để tính tích.")
        return

    m = len(a)
    n = len(a[0])
    min_tich = None
    cot_min = -1

    for j in range(n):
        tich = 1
        for i in range(m):
            tich *= a[i][j]
        if min_tich is None or tich < min_tich:
            min_tich = tich
            cot_min = j

    if cot_min != -1:
        print(f"Cột có tích nhỏ nhất: {cot_min} với tích là {min_tich}")
    else:
        print("Không tìm thấy cột nào.")

