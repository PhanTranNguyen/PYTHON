# g. TBC các phần tử chẵn thuộc dòng lẻ
def tbc_chan_dong_le(a):
    if not a or not a[0]:
        print("Mảng rỗng hoặc không có phần tử để tính TBC (chẵn, dòng lẻ).")
        return

    tong = 0
    dem = 0
    for i in range(len(a)):
        if i % 2 == 1: # Dòng lẻ (index 1, 3, ...)
            for j in range(len(a[0])):
                if a[i][j] % 2 == 0: # Phần tử chẵn
                    tong += a[i][j]
                    dem += 1
    if dem > 0:
        print(f"TBC các phần tử chẵn thuộc dòng lẻ = {tong / dem}")
    else:
        print("Không có phần tử chẵn thuộc dòng lẻ phù hợp.")

# h. TBC các phần tử biên
def tbc_bien(a):
    if not a or not a[0]:
        print("Mảng rỗng hoặc không có phần tử để tính TBC biên.")
        return

    m = len(a)
    n = len(a[0])
    tong = 0
    dem = 0

    if m == 1 and n == 1: # Trường hợp đặc biệt 1x1
        tong = a[0][0]
        dem = 1
    elif m == 1: # Trường hợp 1xn
        for j in range(n):
            tong += a[0][j]
            dem += 1
    elif n == 1: # Trường hợp mx1
        for i in range(m):
            tong += a[i][0]
            dem += 1
    else:
        for i in range(m):
            for j in range(n):
                if i == 0 or i == m - 1 or j == 0 or j == n - 1:
                    tong += a[i][j]
                    dem += 1

    if dem > 0:
        print(f"TBC biên = {tong / dem}")
    else:
        print("Không có phần tử biên phù hợp để tính TBC.")

