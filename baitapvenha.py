def doi_am(a):
    n = len(a)
    for i in range(n):
        for j in range(n):
            if a[i][j] < 0:
                a[i][j] = abs(a[i][j])
#cau c
def thay_chan(a, x):
    n = len(a)
    for i in range(n):
        for j in range(n):
            if a[i][j] % 2 == 0:
                a[i][j] = x
#cau d
def toan_chan(a):
    for row in a:
        for x in row:
            if x % 2 != 0:
                return False
    return True
#cau e
def doi_xung(a, n):
    for i in range(n):
        for j in range(n):
            if a[i][j] != a[j][i]:
                return False
    return True
#cau f
def cheo_chinh_tang(a, n):
    for i in range(n - 1):
        if a[i][i] > a[i+1][i+1]:
            return False
    return True
#cau g
def xuat_tam_giac_duoi(a, n):
    print("Tam giác dưới:")
    for i in range(n):
        for j in range(n):
            if i + j >= n - 1:
                print(a[i][j], end=" ")
        print()
