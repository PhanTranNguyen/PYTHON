#Bai 1
def tron_mang(a, b):
    c = []
    n = min(len(a), len(b))

    # lấy min từng phần tử
    for i in range(n):
        c.append(min(a[i], b[i]))

    # thêm phần dư
    if len(a) > len(b):
        c.extend(a[n:])
    else:
        c.extend(b[n:])

    return c


