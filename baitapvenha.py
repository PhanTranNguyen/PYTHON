def nhap_ma_tran(n):
    a = []
    for i in range(n):
        while True:
            try:
                row_str = input(f"Nhập {n} số cho dòng {i} (cách nhau bởi dấu cách): ")
                row = list(map(int, row_str.split()))
                if len(row) == n:
                    a.append(row)
                    break
                else:
                    print(f"Lỗi: Vui lòng nhập đúng {n} số.")
            except ValueError:
                print("Lỗi: Vui lòng nhập các số nguyên.")
    return a

